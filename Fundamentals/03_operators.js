// An operator allows us to transform or combine multiple values

// Mathematical operator
const now = 2024;
const age = now - 2000;
console.log(age);

console.log(age * 2, age / 10, 2 ** 3) //  2**3 return 2*2*2 which is 8;

// Assignment operators

let x = 10+5;
x += 10             // x = x + 10 
x *= 10             // x = x * 10
x /= 10             // x = x / 10

x++                 // x = x + 1
x--                 // x = x-1


// Comparision operator  (>, <, >=, <=) returns true or false based on the comparision
